/*!
    \note 和xrender cursor 无关
*/
xcb_render_picture_t QMrOfsCursorImage::createCursorXRender(xcb_pixmap_t *pix, xcb_render_picture_t *pic)
{
#ifdef XCB_USE_RENDER
    xcb_connection_t *conn = xcbScreen->xcb_connection();
    const int w = cursorImage.width();
    const int h = cursorImage.height();
    xcb_generic_error_t *error = 0;
    xcb_render_query_pict_formats_cookie_t formatsCookie = xcb_render_query_pict_formats(conn);
    xcb_render_query_pict_formats_reply_t *formatsReply = xcb_render_query_pict_formats_reply(conn,
                                                                                              formatsCookie,
                                                                                              &error);
    if (!formatsReply || error) {
        qWarning("QMrOfsCursorImage::createCursorXRender: query_pict_formats failed");
        free(formatsReply);
        free(error);
        return XCB_NONE;
    }
    xcb_render_pictforminfo_t *fmt = xcb_render_util_find_standard_format(formatsReply, stdPictFormatForDepth(32));
    if (!fmt) {
        qWarning("QMrOfsCursorImage::createCursorXRender: Failed to find format PICT_STANDARD_ARGB_32");
        free(formatsReply);
        return XCB_NONE;
    }
    
    xcb_image_t *ximg = xcb_image_create(w, h, XCB_IMAGE_FORMAT_Z_PIXMAP,
                                       32, 32, 32, 32,
                                       QSysInfo::ByteOrder == QSysInfo::BigEndian ? XCB_IMAGE_ORDER_MSB_FIRST : XCB_IMAGE_ORDER_LSB_FIRST,
                                       XCB_IMAGE_ORDER_MSB_FIRST,
                                       0, 0, 0);
    if (!ximg) {
        qWarning("QMrOfsCursorImage::createCursorXRender: xcb_image_create failed");
        free(formatsReply);
        return XCB_NONE;
    }
    ximg->data = (uint8_t *) malloc(ximg->stride * h);
    if (!ximg->data) {
        qWarning("QMrOfsCursorImage::createCursorXRender: Failed to malloc() image data");
        xcb_image_destroy(ximg);
        free(formatsReply);
        return XCB_NONE;
    }
    memcpy(ximg->data, cursorImage.constBits(), cursorImage.byteCount());

    *pix = xcb_generate_id(conn);
    error = xcb_request_check(conn, xcb_create_pixmap_checked(conn, 32, *pix, xcbScreen->root(), w, h));        //create and hold it
    if (error) {
        qFatal("QMrOfsCursorImage::createCursorXRender, xcb_create_pixmap_checked failed: error code(%d), pixmap(%d), depth(%d), width(%d), height(%d)",  
                error->error_code, *pix, 32, w, h);
    }
    
    *pic = xcb_generate_id(conn);
    error = xcb_request_check(conn, xcb_render_create_picture_checked(conn, *pic, *pix, fmt->id, 0, 0));    //create and hold it
    if (error) {
        qFatal("QMrOfsCursorImage::createCursorXRender, xcb_render_create_picture failed: error code(%d), pict(0x%x), pixmap(0x%x), fmtId(%d)", 
            error->error_code, *pic, *pix, fmt->id);
    }
    
    xcb_gcontext_t gc = xcb_generate_id(conn);
    xcb_create_gc(conn, gc, *pix, 0, 0);
    xcb_image_put(conn, *pix, gc, ximg, 0, 0, 0);                                  //put to pixmap
    xcb_free_gc(conn, gc);

    free(ximg->data);
    xcb_image_destroy(ximg);
    
    qWarning("QMrOfsCursorImage::createCursorXRender: pix(%x), pic(%x), w(%d), h(%d)", *pix, *pic, w, h);
    return *pic;
#else
    return XCB_NONE;
#endif
}

/*!
    create system cursor and xcb pixmap, xcb render picture
*/
void QMrOfsCursorImage::createSystemCursor(int id)
{
    if (!systemCursorTableInit) {
        for (int i = 0; i <= Qt::LastCursor; i++) {
            systemCursorTable[i] = 0;
        }
        systemCursorTableInit = true;
    }
    
    switch (id) {
        // 16x16 cursors
        case Qt::ArrowCursor:
            systemCursorTable[Qt::ArrowCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_arrow_bits, mcur_arrow_bits, 16, 16, 0, 0);
            break;

        case Qt::UpArrowCursor:
            systemCursorTable[Qt::UpArrowCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_up_arrow_bits, mcur_up_arrow_bits, 16, 16, 7, 0);
            break;

        case Qt::CrossCursor:
            systemCursorTable[Qt::CrossCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_cross_bits, mcur_cross_bits, 16, 16, 7, 7);
            break;

        case Qt::IBeamCursor:
            systemCursorTable[Qt::IBeamCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_ibeam_bits, mcur_ibeam_bits, 16, 16, 7, 7);
            break;

        case Qt::SizeVerCursor:
            systemCursorTable[Qt::SizeVerCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_ver_bits, mcur_ver_bits, 16, 16, 7, 7);
            break;

        case Qt::SizeHorCursor:
            systemCursorTable[Qt::SizeHorCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_hor_bits, mcur_hor_bits, 16, 16, 7, 7);
            break;

        case Qt::SizeBDiagCursor:
            systemCursorTable[Qt::SizeBDiagCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_bdiag_bits, mcur_bdiag_bits, 16, 16, 7, 7);
            break;

        case Qt::SizeFDiagCursor:
            systemCursorTable[Qt::SizeFDiagCursor] =
                new QMrOfsCursorImage(xcbScreen, cur_fdiag_bits, mcur_fdiag_bits, 16, 16, 7, 7);
            break;

        case Qt::BlankCursor:
            systemCursorTable[Qt::BlankCursor] =
                new QMrOfsCursorImage(xcbScreen, 0, 0, 0, 0, 0, 0);
            break;

        // 20x20 cursors
        case Qt::ForbiddenCursor:
            systemCursorTable[Qt::ForbiddenCursor] =
                new QMrOfsCursorImage(xcbScreen, forbidden_bits, forbiddenm_bits, 20, 20, 10, 10);
            break;

        // 32x32 cursors
        case Qt::WaitCursor:
            systemCursorTable[Qt::WaitCursor] =
                new QMrOfsCursorImage(xcbScreen, wait_data_bits, wait_mask_bits, 32, 32, 15, 15);
            break;

        case Qt::SplitVCursor:
            systemCursorTable[Qt::SplitVCursor] =
                new QMrOfsCursorImage(xcbScreen, vsplit_bits, vsplitm_bits, 32, 32, 15, 15);
            break;

        case Qt::SplitHCursor:
            systemCursorTable[Qt::SplitHCursor] =
                new QMrOfsCursorImage(xcbScreen, hsplit_bits, hsplitm_bits, 32, 32, 15, 15);
            break;

        case Qt::SizeAllCursor:
            systemCursorTable[Qt::SizeAllCursor] =
                new QMrOfsCursorImage(xcbScreen, size_all_data_bits, size_all_mask_bits, 32, 32, 15, 15);
            break;

        case Qt::PointingHandCursor:
            systemCursorTable[Qt::PointingHandCursor] =
                new QMrOfsCursorImage(xcbScreen, phand_bits, phandm_bits, 32, 32, 0, 0);
            break;

        case Qt::WhatsThisCursor:
            systemCursorTable[Qt::WhatsThisCursor] =
                new QMrOfsCursorImage(xcbScreen, whatsthis_bits, whatsthism_bits, 32, 32, 0, 0);
            break;
        case Qt::BusyCursor:
            systemCursorTable[Qt::BusyCursor] =
                new QMrOfsCursorImage(xcbScreen, busy_bits, busym_bits, 32, 32, 0, 0);
            break;

        case Qt::OpenHandCursor:
            systemCursorTable[Qt::OpenHandCursor] =
                new QMrOfsCursorImage(xcbScreen, openhand_bits, openhandm_bits, 16, 16, 8, 8);
            break;
        case Qt::ClosedHandCursor:
            systemCursorTable[Qt::ClosedHandCursor] =
                new QMrOfsCursorImage(xcbScreen, closedhand_bits, closedhandm_bits, 16, 16, 8, 8);
            break;
        default:
            qWarning("createSystemCursor: Unknown system cursor %d", id);
    }
}

void QMrOfsCursorImage::set(Qt::CursorShape id)
{
    QMrOfsCursorImage *cursor = 0;
    if (id >= 0 && id <= Qt::LastCursor) {
        if (!systemCursorTable[id])
            createSystemCursor(id);
        cursor = systemCursorTable[id];
    }

    if (cursor == 0) {
        if (!systemCursorTable[Qt::ArrowCursor])
            createSystemCursor(Qt::ArrowCursor);
        cursor = systemCursorTable[Qt::ArrowCursor];
    }
    cursorImage = cursor->cursorImage;
    hot = cursor->hot;
    xcbPix = cursor->xcbPix;
    xcbPic = cursor->xcbPic;
}

