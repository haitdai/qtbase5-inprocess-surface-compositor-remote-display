/*!
    原来是改成 3 bpp(indexd8, 只取3色)，这里为了配合render，改成argb 32bpp(取3色)
*/
void QMrOfsCursorImage::set(const uchar *data, const uchar *mask,
                    int width, int height, int hx, int hy)
{
    hot.setX(hx);
    hot.setY(hy);

    cursorImage = QImage(width, height, QImage::Format_ARGB32_Premultiplied);

    if (!width || !height || !data || !mask || cursorImage.isNull())
        return;

    int bytesPerLine = (width + 7) / 8;
    unsigned int p = 0;                                                 //argb
    int d, m;

    int x = -1, w = 0;

    unsigned int *cursor_data = reinterpret_cast<unsigned int*>(cursorImage.bits());     //argb
    for (int i = 0; i < height; i++)
    {
        for (int j = 0; j < bytesPerLine; j++, data++, mask++)
        {
            for (int b = 0; b < 8 && j*8+b < width; b++)// bitwise
            {
                d = *data & (1 << b);
                m = *mask & (1 << b);
                if (d && m) p = 0xff000000;             //opaque black
                else if (!d && m) p = 0xffffffff;       //opaque white
                else p = 0x00000000;                    //transparent
                cursor_data[j*8+b] = p;

                // calc region
                if (x < 0 && m)
                    x = j*8+b;
                else if (x >= 0 && !m) {
                    x = -1;
                    w = 0;
                }
                if (m)
                    w++;
            }
        }
        if (x >= 0) {
            x = -1;
            w = 0;
        }
        cursor_data += width;
    }

}


